package cc.alessandro.jpocket.filters;

public enum Sort {

	/**
	 * items in order of newest to oldest
	 */
	NEWEST,

	/**
	 * items in order of oldest to newest
	 */
	OLDEST,

	/**
	 * items in order of title alphabetically
	 */
	TITLE,

	/**
	 * items in order of url alphabetically
	 */
	SITE;

}
